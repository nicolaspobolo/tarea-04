class boton{
    constructor(texto, color, alto, ancho){
        this.texto=texto;
        this.color=color;
        this.alto=alto;
        this.ancho=ancho;
    }
}

class video{
    constructor(fuente, resolucion, formato){
        this.fuente=fuente;
        this.resolucion=resolucion;
        this.formato;
    }
}

class audio{
    constructor(fuente, formato){
        this.fuente=fuente;
        this.formato=formato;
    }
}

